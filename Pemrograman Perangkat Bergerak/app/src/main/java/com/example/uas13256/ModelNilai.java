package com.example.uas13256;

public class ModelNilai {
    String kode;
    String mata_kuliah;
    String sks;
    String n_angka;
    String n_huruf;
    String predikat;
    String key;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getMata_kuliah() {
        return mata_kuliah;
    }

    public void setMata_kuliah(String mata_kuliah) {
        this.mata_kuliah = mata_kuliah;
    }

    public String getSks() {
        return sks;
    }

    public void setSks(String sks) {
        this.sks = sks;
    }

    public String getN_angka() {
        return n_angka;
    }

    public void setN_angka(String n_angka) {
        this.n_angka = n_angka;
    }

    public String getN_huruf() {
        return n_huruf;
    }

    public void setN_huruf(String n_huruf) {
        this.n_huruf = n_huruf;
    }

    public String getPredikat() {
        return predikat;
    }

    public void setPredikat(String predikat) {
        this.predikat = predikat;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
